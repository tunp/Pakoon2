int CMainFrame::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CFrameWnd::OnCreate(lpCreateStruct) == -1)
		return -1;

  // Hide the menu that has been created
  SetMenu(NULL);
  CMenu *pMenu = GetMenu();
  if(pMenu) {
    BOOL bDone = pMenu->DestroyMenu();
  }

  // Change display mode to full screen
  EnumDisplaySettings(NULL, ENUM_CURRENT_SETTINGS, &g_devmodeorig);
  DEVMODE devmode = g_devmodeorig;
  SetWindowPos(NULL, -2, -2, devmode.dmPelsWidth + 4, devmode.dmPelsHeight + 4, 0);

  // Initialize Controller Module
  ControllerModule::Initialize(m_hWnd);

	return 0;
}

void CMainFrame::OnDestroy() {
  CFrameWnd::OnDestroy();
}

BOOL CMainFrame::PreCreateWindow(CREATESTRUCT& cs) {
  cs.style = WS_POPUP | WS_CLIPCHILDREN | WS_CLIPSIBLINGS;
	if( !CFrameWnd::PreCreateWindow(cs) )
		return FALSE;

	return TRUE;
}

